#include <unistd.h>
#include <string.h>
#include <stdio.h>

void	ft_putstr(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		write(1, &str[a],1);
		a++;
	}
}

int		main(void)
{
	char	*str = "hello";

	ft_putstr(str);
}
